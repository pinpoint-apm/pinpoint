package com.navercorp.pinpoint.service.dao.dto;

public class ServiceParam {

    private int uid;
    private String name;

    public ServiceParam() {
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
