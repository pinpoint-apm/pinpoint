package com.navercorp.pinpoint.service.vo;

public class ServiceEntity {

    private int uid;
    private String name;

    public ServiceEntity() {
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
